import Foundation
import UIKit

extension UIFont {
    static func getBoldFont() -> String {
        return "NanumGothic-Bold"
    }
    
    static func getExtraBoldFont() -> String {
        return "NanumGothic-ExtraBold"
    }
    
    static func getRegularFont() -> String {
        return "NanumGothic-Regular"
    }
}
