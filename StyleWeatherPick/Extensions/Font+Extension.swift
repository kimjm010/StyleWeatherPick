import Foundation
import UIKit

extension UIFont {
    static func getBoldFont() -> String {
        return "NanumGothicBold"
    }
    
    static func getExtraBoldFont() -> String {
        return "NanumGothicExtraBold"
    }
    
    static func getRegularFont() -> String {
        return "NanumGothic"
    }
}
